# Pedra, papel ou tesoura?
# Nesse exercício vamos criar um programa que simula uma partida de pedra, papel ou tesoura
# O usuário irá competir contra o computador, a jogada do mesmo é calculada de forma randômica
# Para isso, use como base o exercicio 2 deste de modulo
# O programa deve pedir ao usuário que digite qual será a jogada dele
# As opções de jogada devem ser apenas "pedra", "papel" ou "tesoura"
# Qualquer valor diferente desse deve retorna um erro para o usuário
# Após ter capturada a jogada do usuário e o programa ter decididoa jogada do computador (randomico)
# O programa deve ter imprimir qual foi a jogada do computador
# Ao final o programa deve imprimir qual foi o resultado: vitória, derrota ou empate
